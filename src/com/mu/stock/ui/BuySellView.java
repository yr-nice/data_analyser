    public void init(AccountTxnView acctTxnView, Account acct, Company c, String action, String price)
    {
        this.acct = acct;
        this.company = c;
        this.action = action;
        this.acctTxnView = acctTxnView;
        lblAction.setText(action);
        lblCpy.setText(c.getShortName());
        tfPrice.setText(price);
        tfAmount.setText((int)(10000/Float.valueOf(price))+"");
        lblNow.setText(acct.getCash().intValue()+"");
        updateLabels();

    }
    private void updateLabels()
    {
        float p = Float.valueOf(tfPrice.getText());
        int amt = Integer.valueOf(tfAmount.getText());
        float t = p*amt;
        float af = acct.getCash() + p*amt*(action.equals(TxnLog.Buy)? -1 : 1);
        int curAmt = 0;
        ArrayList al = acct.getPortfolio().get(company);
        if(al != null)
        {
            curAmt = (Integer)al.get(0);
        }
        int afAmt = curAmt + amt*(action.equals(TxnLog.Buy)? -1 : 1);

        lblBullet.setText((int)(t/10000)+ " Bullets");
        lblPerc.setText(MathUtil.round(t*100/acct.getCash(), 1)+"%");
        lblTotal.setText((int)t+"");
        lblAfter.setText(String.format("Cash:%d, Shares:%d",(int)af,afAmt));
    }
