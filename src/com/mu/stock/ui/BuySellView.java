    private void tfAmountMouseWheelMoved(java.awt.event.MouseWheelEvent evt)//GEN-FIRST:event_tfAmountMouseWheelMoved
    {//GEN-HEADEREND:event_tfAmountMouseWheelMoved
        if(evt.getScrollType()==java.awt.event.MouseWheelEvent.WHEEL_UNIT_SCROLL)
        {
            int i = (-1)*evt.getUnitsToScroll()/evt.getScrollAmount();
            int now = Integer.valueOf(tfAmount.getText());
            int fact = now/100;
            if(fact<1)
                fact = 1;
            now += i*fact;
            tfAmount.setText(now+"");
        }
        updateLabels();
    }//GEN-LAST:event_tfAmountMouseWheelMoved

    private void tfPriceMouseWheelMoved(java.awt.event.MouseWheelEvent evt)//GEN-FIRST:event_tfPriceMouseWheelMoved
    {//GEN-HEADEREND:event_tfPriceMouseWheelMoved
        if(evt.getScrollType()==java.awt.event.MouseWheelEvent.WHEEL_UNIT_SCROLL)
        {
            int i = (-1)*evt.getUnitsToScroll()/evt.getScrollAmount();
            float now = Float.valueOf(tfPrice.getText());
            float fact = now/100;
            if(fact<0.001)
                fact = 0.001F;
            now += i*fact;
            tfPrice.setText(MathUtil.round(now, 3)+"");
        }
        updateLabels();
        // TODO add your handling code here:
    }//GEN-LAST:event_tfPriceMouseWheelMoved

    private void tfAmountPropertyChange(java.beans.PropertyChangeEvent evt)//GEN-FIRST:event_tfAmountPropertyChange
    {//GEN-HEADEREND:event_tfAmountPropertyChange
        if(!tfPrice.getText().isEmpty()&&!tfAmount.getText().isEmpty())
            updateLabels();
    }//GEN-LAST:event_tfAmountPropertyChange
    
